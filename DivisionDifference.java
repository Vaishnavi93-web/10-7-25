public class DivisionDifference {
    public static void main(String[] args) {
        int a = 7;
        int b = 2;

        int intResult = a / b;

        double doubleResult = (double) a / b;

        System.out.println("Integer division (7 / 2): " + intResult);
        System.out.println("Double division (7 / 2): " + doubleResult); 
    }
}
